  /**
   * 页面的初始数据
   */
  data: {
  
  },

  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    let that = this;
    // 数据
    app.getLoginSession((session) => {
      wx.request({
        url: app.api + '/words/mieWords',
        method: "GET",
        data: {
          t_session: session
        },
        success: function (res) {
          that.setData({
            count:res.data.retval.count,
            list: res.data.retval.list,
          })
        }
      })
    });
  },

